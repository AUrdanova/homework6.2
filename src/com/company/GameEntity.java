package com.company;

public class GameEntity {
    private int commonHealth;
    private int commonDamage;

    public int getCommonHealth() {
        return commonHealth;
    }

    public void setCommonHealth(int commonHealth) {
        this.commonHealth = commonHealth;
    }

    public int getCommonDamage() {
        return commonDamage;
    }

    public void setCommonDamage(int commonDamage) {
        this.commonDamage = commonDamage;
    }
}
